package com.smu.appmod;

public class Advice {

    String anomaly;
    String anomalyDate;
    String adviceGiven;
    String adviceAskedDate;
    String adviceFollowed;
    String seeker;
    String anomalyid;

    public String getAnomaly() {
        return anomaly;
    }

    public void setAnomaly(String anomaly) {
        this.anomaly = anomaly;
    }

    public String getAnomalyId() {
        return anomalyid;
    }

    public void setAnomalyId(String anomalyid) {
        this.anomalyid = anomalyid;
    }

    public String getSeeker() {
        return seeker;
    }

    public void setSeeker(String seeker) {
        this.seeker = seeker;
    }

    public String getAnomalyDate() {
        return anomalyDate;
    }

    public void setAnomalyDate(String anomalyDate) {
        this.anomalyDate = anomalyDate;
    }

    public String getAdviceGiven() {
        return adviceGiven;
    }

    public void setAdviceGiven(String adviceGiven) {
        this.adviceGiven = adviceGiven;
    }

    public String getAdviceAskedDate() {
        return adviceAskedDate;
    }

    public void setAdviceAskedDate(String adviceAskedDate) {
        this.adviceAskedDate = adviceAskedDate;
    }


    public String getAdviceFollowed() {
        return adviceFollowed;
    }

    public void setAdviceFollowed(String adviceFollowed) {
        this.adviceFollowed = adviceFollowed;
    }


}