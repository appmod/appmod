package com.smu.appmod;

public class Dependant {

    String dependantName;
    String dependantPhone;
    String dependantStatus;

    public String getDependantName() {
        return dependantName;
    }

    public void setDependantName(String name) {
        this.dependantName = name;
    }

    public String getDependantPhone() {
        return dependantPhone;
    }

    public void setDependantPhone(String phone) {
        this.dependantPhone = phone;
    }

    public String getDependantStatus() {
        return dependantStatus;
    }

    public void setDependantStatus(String status) {
        this.dependantStatus = status;
    }


}